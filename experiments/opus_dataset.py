"""
experiments/opus_dataset.py
============================
Adaptador para los datasets generados por Claude Opus
(DataSet-Generator-Claude-Opus/mose_distillation_datasets/datasets/).

Expone la misma interfaz .generate() que los generadores sintéticos de AION-C:
    gen = OpusDataset("cora", max_examples=1000)
    ex  = gen.generate(level=1)
    ex.problem_text  # str — campo "input"  del JSONL
    ex.answer        # str — campo "expected_output" del JSONL
    ex.difficulty    # int — 1..5
    ex.subtask       # str
    ex.graph         # dict — grafo pre-construido (nodes, edges)
    ex.reasoning     # str — chain-of-thought de Opus

Formato JSONL esperado (cada línea):
    {
      "id": "cora_00001",
      "motor": "CORA",
      "subtask": "counterfactual",
      "difficulty": 4,
      "input": "...",
      "graph": {"nodes": [...], "edges": [...]},
      "expected_output": "...",
      "reasoning": "...",
      "metadata": {...}
    }
"""

from __future__ import annotations

import json
import random
from pathlib import Path
from types import SimpleNamespace
from typing import List, Optional, Tuple

# ── Rutas por defecto ─────────────────────────────────────────────────────────
# La carpeta DataSet-Generator-Claude-Opus vive junto a AION-C en ias/
_REPO_ROOT = Path(__file__).parent.parent          # AION-C/
_IAS_ROOT  = _REPO_ROOT.parent                     # ias/
_DS_DEFAULT = (
    _IAS_ROOT
    / "DataSet-Generator-Claude-Opus"
    / "mose_distillation_datasets"
    / "datasets"
)

MOTOR_FILES: dict[str, str] = {
    "cora":         "mose_cora.jsonl",
    "axiom":        "mose_axiom.jsonl",
    "empathy":      "mose_empathy.jsonl",
    "forge_c":      "mose_forge_c.jsonl",
    "muse":         "mose_muse.jsonl",
    "orchestrator": "mose_orchestrator.jsonl",
}


class OpusDataset:
    """
    Wraps a JSONL dataset file generated by Claude Opus.

    API compatible con los generadores sintéticos de AION-C:
        ex = dataset.generate(level=1)  # 'level' ignorado, filtrar con difficulty_range
        ex.problem_text  → input del ejemplo
        ex.answer        → expected_output del ejemplo

    Args:
        motor:            Nombre del motor: "cora", "axiom", "empathy",
                          "forge_c", "muse", "orchestrator".
        max_examples:     Si se especifica, limita el número de ejemplos cargados.
        difficulty_range: Tuple (min_diff, max_diff) para filtrar por dificultad (1-5).
        seed:             Semilla para .generate() reproducible y para el muestreo
                          de max_examples.
        dataset_root:     Ruta alternativa a la carpeta de datasets.
                          Si None, usa DataSet-Generator-Claude-Opus junto a AION-C.
    """

    def __init__(
        self,
        motor: str,
        max_examples: Optional[int] = None,
        difficulty_range: Optional[Tuple[int, int]] = None,
        seed: Optional[int] = None,
        dataset_root: Optional[Path] = None,
    ) -> None:
        motor_key = motor.lower().replace("-", "_")
        if motor_key not in MOTOR_FILES:
            raise ValueError(
                f"Motor desconocido: '{motor}'. "
                f"Opciones: {list(MOTOR_FILES.keys())}"
            )

        if dataset_root is not None:
            root = Path(dataset_root)
        else:
            # Try multiple locations
            root = _DS_DEFAULT
            # Also check AION-C/datasets/opus/ (H200 package layout)
            _opus_local = _REPO_ROOT / "datasets" / "opus"
            if not (root / MOTOR_FILES[motor_key]).exists() and (_opus_local / MOTOR_FILES[motor_key]).exists():
                root = _opus_local

        path = root / MOTOR_FILES[motor_key]
        if not path.exists():
            raise FileNotFoundError(
                f"Dataset no encontrado: {path}\n"
                f"Buscado en: {_DS_DEFAULT} y {_REPO_ROOT / 'datasets' / 'opus'}"
            )

        with open(path, encoding="utf-8") as fh:
            raw: List[dict] = [json.loads(line) for line in fh if line.strip()]

        # Filtrar por dificultad
        if difficulty_range is not None:
            lo, hi = difficulty_range
            raw = [ex for ex in raw if lo <= ex.get("difficulty", 0) <= hi]

        # Muestrear max_examples
        if max_examples is not None and max_examples < len(raw):
            rng_sample = random.Random(seed)
            raw = rng_sample.sample(raw, max_examples)

        if not raw:
            raise ValueError(
                f"Dataset vacío después de filtrar "
                f"(motor={motor}, difficulty_range={difficulty_range}, "
                f"max_examples={max_examples})"
            )

        self._examples = raw
        self._rng      = random.Random(seed)
        self.motor     = motor_key

    # ── API pública ───────────────────────────────────────────────────────────

    def __len__(self) -> int:
        return len(self._examples)

    def generate(self, level: Optional[int] = None) -> SimpleNamespace:
        """
        Retorna un ejemplo aleatorio del dataset.

        El argumento `level` se ignora (compatibilidad con generadores sintéticos).
        Para filtrar por dificultad, usa `difficulty_range` en el constructor.

        Returns:
            SimpleNamespace con campos:
                .problem_text  → str  (campo "input")
                .answer        → str  (campo "expected_output")
                .difficulty    → int  (1–5)
                .subtask       → str
                .graph         → dict {"nodes": [...], "edges": [...]}
                .reasoning     → str  (chain-of-thought de Opus)
        """
        ex = self._rng.choice(self._examples)
        return SimpleNamespace(
            problem_text = ex["input"],
            answer       = ex["expected_output"],
            difficulty   = ex.get("difficulty", 1),
            subtask      = ex.get("subtask", ""),
            graph        = ex.get("graph", {}),
            reasoning    = ex.get("reasoning", ""),
        )

    def get_all_texts(self) -> List[str]:
        """
        Retorna todos los textos (input + expected_output) para construir
        un Tokenizer word-level a partir del corpus Opus.
        """
        return [ex["input"] + " " + ex["expected_output"]
                for ex in self._examples]

    def train_eval_split(
        self,
        eval_size: int = 100,
        seed: Optional[int] = None,
    ) -> Tuple["OpusDataset", "OpusDataset"]:
        """
        Divide el dataset en train y eval sin solapamiento.

        Returns:
            (train_dataset, eval_dataset)
        """
        rng = random.Random(seed)
        indices = list(range(len(self._examples)))
        rng.shuffle(indices)

        eval_idx  = set(indices[:eval_size])
        train_idx = set(indices[eval_size:])

        train_ds = _OpusSubset(self, list(train_idx), seed=seed)
        eval_ds  = _OpusSubset(self, list(eval_idx),  seed=seed)
        return train_ds, eval_ds

    @property
    def examples(self) -> List[dict]:
        """Public access to the loaded examples."""
        return self._examples

    def __repr__(self) -> str:
        return (
            f"OpusDataset(motor={self.motor!r}, "
            f"n_examples={len(self._examples)})"
        )


class _OpusSubset(OpusDataset):
    """Vista de un subconjunto de OpusDataset (sin recargar el archivo)."""

    def __init__(self, parent: OpusDataset, indices: List[int],
                 seed: Optional[int] = None) -> None:
        # Bypasear __init__ del padre — montar directamente
        object.__setattr__(self, "_examples", [parent._examples[i] for i in indices])
        object.__setattr__(self, "_rng",      random.Random(seed))
        object.__setattr__(self, "motor",     parent.motor)
